package com.mokoji.domain;

public class GifticonVO {
	private String gcon_name, gcon_rpic;
	private int gcon_code, gcon_price, gcon_count;
	
	public int getGcon_count() {
		return gcon_count;
	}
	public void setGcon_count(int gcon_count) {
		this.gcon_count = gcon_count;
	}
	public String getGcon_name() {
		return gcon_name;
	}
	public void setGcon_name(String gcon_name) {
		this.gcon_name = gcon_name;
	}
	public String getGcon_rpic() {
		return gcon_rpic;
	}
	public void setGcon_rpic(String gcon_rpic) {
		this.gcon_rpic = gcon_rpic;
	}
	public int getGcon_code() {
		return gcon_code;
	}
	public void setGcon_code(int gcon_code) {
		this.gcon_code = gcon_code;
	}
	public int getGcon_price() {
		return gcon_price;
	}
	public void setGcon_price(int gcon_price) {
		this.gcon_price = gcon_price;
	}
	
	

}
